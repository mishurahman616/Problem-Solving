#include<bits/stdc++.h>
using namespace std;

int main(){
    int a;
    int i, j,ti, tj, x, y;
    for(i=1; i<6; i++){
        for(j=1; j<6; j++){
            cin>> a;
            if(a==1){
                ti=i;
                tj=j;
            }
        }

    }
    x=abs(3-ti);
    y=abs(3-tj);
    cout << x+y;

}
